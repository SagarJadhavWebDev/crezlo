export const createCustomTheme = (customizations: Partial<Theme>): Theme => {
  const baseTheme = customizations.mode === 'dark' ? defaultDarkTheme : defaultLightTheme;
  
  return {
    ...baseTheme,
    ...customizations,
    colors: {
      ...baseTheme.colors,
      ...(customizations.colors || {}),
    },
    spacing: {
      ...baseTheme.spacing,
      ...(customizations.spacing || {}),
    },
    typography: {
      ...baseTheme.typography,
      ...(customizations.typography || {}),
    },
    borderRadius: {
      ...baseTheme.borderRadius,
      ...(customizations.borderRadius || {}),
    },
    shadow: {
      ...baseTheme.shadow,
      ...(customizations.shadow || {}),
    },
  };
};

